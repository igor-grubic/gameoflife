const getSeedArray = stringSeed => {
  const seed = [];
  let x = 0;
  stringSeed.forEach(el => {
    for (let y = 0; y < el.s.length; y++) {
      if (el.s[y] === 'O') seed.push([x, y]);
    }
    x++;
  });
  return seed;
};

